pub fn label_for_code(code: &i32) -> &'static str {
    for (l, c) in KEYS {
        if c == code {
            return l;
        }
    }
    return "None";
}

pub fn code_for_label(label: &str) -> i32 {
    for (l, c) in KEYS {
        if *l == label {
            return c.clone();
        }
    }
    return 0;
}
